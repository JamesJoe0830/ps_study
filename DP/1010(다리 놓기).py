import sys
T = int(input())

def factorial(n):
    num = 1
    while n >= 1 :
        num *= n
        n -= 1
    return num

for test_case in range(T):
    N, M = map(int,input().split())
    answer = factorial(M) /(factorial(M-N)*factorial(N))
    print(int(answer))

