def solution(n, computers):
    answer = 0
    visited = []
    # recursion DFS
    def DFS(v):
        visited.append(v)
        for i in range(n):
            if computers[v][i] == 1 and i not in visited:
                DFS(i)
    for i in range(n):
        if i not in visited:
            DFS(i)
            answer +=1
    
    return answer
