def dijkstra(start):
    q = []
    heapq.heappush(q,(0,start))
    cost[start] = 0 #ì‹œì‘ ì‹œì ì˜ ë¹„ìš©ì€ 0ì´ë¯€ë¡œ
    while q:
        # print("â°",q)

        pay, now = heapq.heappop(q)

        if cost[now] < pay: # ë¹„êµí•˜ê¸° ìœ„í•œ ë¹„ìš©ì´ ì—¬íƒœê¹Œì§€ì˜ costë“¤ì¤‘ ì„ ì •ëœ ë¹„ìš©ë³´ë‹¤ í¬ë©´ ì•„ë˜ ìƒëµ [â° ì´ì¤„ ì—†ìœ¼ë©´ ì‹œê°„ì´ˆê³¼ ë°œìƒ]
            continue

        for e, c in graph[now]:
            now_cost = pay + c
            # print("ğŸŒ™",now_cost,pay,c)
            if now_cost < cost[e]: # ë„ì°©í–ˆì§€ì ê¹Œì§€ì˜ ë¹„ìš© < ë„ì°©ì§€ì˜ ë¹„ìš© (ì¦‰, ë” ì‹¼ ë¹„ìš© ì§€ë¶ˆí•œ ê²½ìš°)
                cost[e] = now_cost
