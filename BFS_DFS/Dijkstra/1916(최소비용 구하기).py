cost = [INF] * (N+1) #[INF, INF, INF,INF,INF]
graph = [[] for _ in range(N+1)]
for i in range(M):
    s,e, c = map(int,input().split()) # s: start, e: end, c: cost
    graph[s].append((e,c))
