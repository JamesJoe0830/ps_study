import heapq # ìš°ì„ ìˆœìœ„ í ë¼ì´ë¸ŒëŸ¬ë¦¬ O(logN)
import sys
input = sys.stdin.readline

INF = int(1e9) #ë¬´í•œì„ ì˜ë¯¸í•˜ëŠ” ê°’ìœ¼ë¡œ 10ì–µì„ ì„¤ì •

# N:ì§€ë¦„ê¸¸ì˜ ê°œìˆ˜, D:ê³ ì†ë„ë¡œ ê¸¸ì´
N, D = map(int,input().split())
graph = [[] for _ in range(D+1)]
# ìµœë‹¨ ê±°ë¦¬ë¥¼ ì¼ë‹¨ ë¬´í•œìœ¼ë¡œ ì´ˆê¸°í™”
distance = [INF] * (D+1)
# ì¼ë‹¨ ê±°ë¦¬ë¥¼ 1ë¡œ ì´ˆê¸°í™”
for i in range(D):
    graph[i].append((i+1, 1))

# ì§€ë¦„ê¸¸ì´ ìžˆìœ¼ë©´ ì—…ë°ì´íŠ¸
for _ in range(N):
    start, end, length = map(int,input().split())
    if end > D : #ë§Œì¼ ê³ ì†ë„ë¡œ ê¸¸ì´ë³´ë‹¤ endê°’ì´ í¬ë©´ ë¬´ì‹œ
        continue
    graph[start].append((end,length))
    # startì— endì™€ lengthê°’ ë„£ì–´ì£¼ê¸°

# ë‹¤ìµìŠ¤íŠ¸ë¼ ì •ì˜    
def dijkstra(start):
    Que=[]
