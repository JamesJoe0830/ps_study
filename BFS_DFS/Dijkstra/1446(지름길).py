    distance[start]=0
    while Que:
        dist, now = heapq.heappop(Que)  # distëŠ” ì—¬íƒœê¹Œì§€ ê±°ë¦¬

        if dist > distance[now]:
            continue
        for i in graph[now]:
            now_dist= dist +i[1]
            if now_dist < distance[i[0]]:  # í˜„ìž¬ê±°ë¦¬ < endê°’ (ì¦‰, ì§€ë¦„ê¸¸ì„ íƒí–ˆì„ ë•Œê°€ ë” ì§§ì€ ê²½ìš°)
                distance[i[0]] = now_dist # ê±°ë¦¬ë¥¼ ì§€ë¦„ê¸¸ë¡œ ëŒ€ì²´í•¨
                heapq.heappush(Que,(now_dist, i[0])) #(í˜„ìž¬ê¹Œì§€ê±°ë¦¬, í˜„ìž¬ì‹œì )



