import sys
input = sys.stdin.readline
N = int(input())
graph = [list(input().rstrip()) for _ in range(N)]
dx = [0,0,-1,1]
dy = [-1,1,0,0]

# recursion DFS
def dfs(y,x):
    global house
    house += 1
    
    graph[y][x] = '0' # visited ì²˜ë¦¬
    for i in range(4):
        ny = y + dy[i]
        nx = x + dx[i]
        if 0 <= ny < N and 0 <= nx < N:
            if graph[ny][nx] == '1':
                dfs(ny,nx)
    return house

answer = []
for y in range(N):
    for x in range(N):
        if graph[y][x] == '1':
            house = 0
            answer.append(dfs(y,x))

print(len(answer))
answer.sort()
for ans in answer:
    print(ans)
