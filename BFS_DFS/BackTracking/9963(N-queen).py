# ìœ ë§í•œì§€ ê²€ì‚¬
def promising(x) : 
    for i in range(x):
        # 0ì—ì„œ x ê¹Œì§€ ê°™ì€ ì»¬ëŸ¼ì— í€¸ì´ ìˆëŠ”ì§€ ì—†ëŠ”ì§€ , ëŒ€ê°ì„ ì— í€¸ì´ ìˆëŠ”ì§€ ì—†ëŠ”ì§€
        if row[x] == row[i] or abs(row[x]-row[i]) == abs(x-i) :
            return False
    return True

def n_queen(x):
    global answer
    if x == N:
        answer +=1
        return
    
    else:
        for i in range(N):
            row[x] = i
            # ìœ ë§í•˜ë‹¤ë©´ n_queenì„ ê³„ì† ê¹Šì´ íƒìƒ‰
            if promising(x): 
                n_queen(x+1)

n_queen(0)
