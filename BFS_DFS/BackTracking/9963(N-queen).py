import sys
input = sys.stdin.readline
N = int(input())
# rowëŠ” queenì´ ê°ê° rowë§ˆë‹¤ ì–´ë””ì— ìœ„ì¹˜í•´ìˆëŠ”ì§€ [1,3,0,2] -> (0,1) (1,3) (2,0) (3,2) ì— í€¸ì´ ìˆë‹¤.
row = [0] * N
