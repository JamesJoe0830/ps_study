import sys
input = sys.stdin.readline
N = int(input())
nums = list(map(int,input().split()))
nums.sort()
answer = 0
for i in range(N):
    except_self = nums[:i]+nums[i+1:] #μκΈ° μμ‹ μ„ μ μ™Έν• λ°°μ—΄ μƒμ„±
    start = 0
    end = len(except_self) -1

    while start < end:
        total = except_self[start] + except_self[end]
        if total == nums[i]:
            answer +=1 
            break
        
        elif total < nums[i]: # numsλ°°μ—΄λ³΄λ‹¤ totalμ΄ μ‘μ„ κ²½μ°μ— startκ°’μ„ λλ ¤μ¤€λ‹¤.  # 1 2 3 4 6μ—μ„ ν„μ¬ nums[i]κ°€ 6λ©΄ start 1 end 4 μΌλ–„ 
            start +=1
