import sys
input = sys.stdin.readline


# find ì—°ì‚°
def find_parent(parent,x):
  if parent[x] != x :
    parent[x] = find_parent(parent, parent[x])
  return parent[x]

# union ì—°ì‚°
def union_parent(parent, a, b):
  a = find_parent(parent,a)
  b = find_parent(parent,b)
  if a < b:
    parent[b] = a
  else:
    parent[a] = b


while True :
  m, n = map(int,input().split())
  # m: ì§‘ì˜ ìˆ˜ n : ê¸¸ì˜ ìˆ˜
  if m == 0 and n == 0:
    break
  parent = [0] * (m+1)
  city = []
  answer = 0
  total_length = 0

  # ë¶€ëª¨ í…Œì´ë¸” ì´ˆê¸°í™”
  for i in range(1,m+1):
    parent[i] = i
  # [0,1,2,3,4,5,6,7]

  for i in range(n):
    x, y, z = map(int,input().split())
    # x, y: ì§‘ / z: ê±°ë¦¬
    city.append((z,x,y))
    answer += z
  city.sort()

  
  # í¬ë£¨ìŠ¤ì¹¼ì€ ê°„ì„ ì˜ ì •ë³´ë¥¼ í•˜ë‚˜ì”© í™•ì¸í•˜ë©´ì„œ ì§„í–‰í•œë‹¤.
  for i in range(n):
    length, x, y = city[i]

    # find ì—°ì‚°ì„ í†µí•´ì„œ ë¶€ëª¨ê°€ ë‹¤ë¥´ë‹¤ == ì‚¬ì´í´ X -> ìµœì†Œ ì‹ ì¥ íŠ¸ë¦¬ì— í¬í•¨!
    if find_parent(parent, x) != find_parent(parent, y):
      union_parent(parent, x, y)
      total_length += length
  print(answer - total_length)

