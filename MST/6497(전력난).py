import sys
from heapq import heappop, heappush
input = sys.stdin.readline


# m: ì§‘ì˜ ìˆ˜, n: ê¸¸ì˜ ìˆ˜
while True:
  m, n = map(int ,input().split())
  if m == 0 and n == 0 :
    break
  town = [[] for _ in range(m)]
  visited = [0]*m
  total = 0

  for i in range(n):
    x, y ,z  = map(int,input().split())
    town[x].append((z,y))
    town[y].append((z,x))
    total += z


  def mst():
    q = []
    q.append((0,0))
    use_cost = 0
    while q :
      cost, node = heappop(q)
      if visited[node] == 0 :
        use_cost += cost
        visited[node] = 1
        for next_cost, next in town[node]:
          if visited[next] == 0:
            heappush(q,(next_cost,next))
    return (total-use_cost)

  print(mst())

