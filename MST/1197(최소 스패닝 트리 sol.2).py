import sys
from heapq import heapify, heappop, heappush
sys.stdin.readline
V, E = map(int, input().split())
# V: ì •ì ì˜ ê°œìˆ˜ E: ê°„ì„ ì˜ ê°œìˆ˜
nodes = [[] for _ in range(V+1)]
for _ in range(E):
  A, B, C = map(int, input().split())
  nodes[A].append((C,B))
  nodes[B].append((C,A))
visited = [0] * (V+1)

def prim():
  q = []
  q.append((0,1))
  answer, cnt = 0, 0

  while q :
    if cnt == V :
      break

    length, node = heappop(q)
    if visited[node] == 0 :
      visited[node] += 1
      answer += length
      cnt += 1 
      for next_length, next_node in nodes[node]:
            heappush(q,(next_length,next_node))
  return print(answer)
  
prim()

