import sys
import heapq
input = sys.stdin.readline

N = int(input())
M = int(input())
graph=[[] for _ in range(N+1)]
visited = [0 for _ in range(N+1)]

for i in range(M):
    a,b,c = map(int,input().split())
    graph[a].append((c,b)) # heapqíŠ¹ì„±ìƒ ê°€ì¤‘ì¹˜ë¥¼ ê¸°ì¤€ìœ¼ë¡œ ì •ë ¬ í•˜ë¯€ë¡œ ì´ë ‡ê²Œ ë„£ì–´ì¤€ë‹¤.
    graph[b].append((c,a))

def prim():
    q = []
    q.append((0,1)) # heapqíŠ¹ì„±ìƒ ê°€ì¤‘ì¹˜ë¥¼ ê¸°ì¤€ìœ¼ë¡œ ì •ë ¬ í•˜ë¯€ë¡œ ì´ë ‡ê²Œ ë½‘ëŠ”ë‹¤.
    answer = 0 
    while q:
        len, node = heapq.heappop(q)
        if visited[node] == 0 :
            visited[node] = 1
            answer +=len
            for next_len,next_node in graph[node]:
                heapq.heappush(q,(next_len,next_node))
    return answer

print(prim())

