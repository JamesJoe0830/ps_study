import sys
from heapq import heappop, heappush
input = sys.stdin.readline

N = int(input())
M = int(input())

network = [[] for _ in range(N+1)]
visited = [0 for _ in range(N+1)]

for i in range(M):
    a, b, cost = map(int, input().split())
    network[a].append((cost,b))
    network[b].append((cost,a))

def prim():
    answer = 0
    heap = []
    heappush(heap,(0,1))
    while heap:
        cost, node = heappop(heap)
        if visited[node] == 0:
            visited[node] = 1
            answer += cost
            for next_cost, next_node in network[node]:
                    heappush(heap,(next_cost,next_node))
    return answer
print(prim())

