import sys 
input = sys.stdin.readline

M, N = map(int,input().split())
cookies = list(map(int,input().split()))

start = 1
end = max(cookies) #ìµœëŒ€ë¡œ ìë¥´ëŠ” ì¿ í‚¤ í¬ê¸°

while start <= end :
    mid = (start + end)//2 #ì¿ í‚¤ ìë¥´ëŠ” í¬ê¸°
    cnt = 0
    for cookie in cookies :
        if mid <= cookie :
            cnt += cookie//mid
    if cnt >= M :
        start = mid + 1
    else:
        end = mid - 1
print(end)

