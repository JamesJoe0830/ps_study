import sys
input = sys.stdin.readline
# K: ì´ë¯¸ ê°–ê³  ìˆëŠ” ëœì„ ì˜ ê°œìˆ˜ N : í•„ìš”í•œ ëœì„ ì˜ ê°œìˆ˜
K, N = map(int,input().split())
Cables = [int(input()) for _ in range(K)]
Cables.sort()
start = 1
end = Cables[-1]

# ì´ì§„ íƒìƒ‰(Binary Search)
while start <= end:
    mid = (start + end)//2 
    slice_cnt = 0
    for c in Cables:
        slice_cnt += c//mid
    if slice_cnt >= N :
        start = mid + 1
    elif slice_cnt < N :
        end = mid - 1
print(end)
