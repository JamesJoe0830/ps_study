import sys
input = sys.stdin.readline

# N : ë‚˜ë¬´ì˜ ìˆ˜ / M : ë‚˜ë¬´ì˜ ê¸¸ì´
N, M = map(int,input().split())
trees_length = list(map(int,input().split()))
trees_length.sort()
start = 1
end = trees_length[-1]


while start <= end :
    mid = (start + end) //2
    

    ans = 0
    for i in range(N):
        if trees_length[i] - mid > 0 :
            ans += trees_length[i] - mid
  
    if ans >= M :
        start = mid + 1
    elif ans < M :
        end =  mid - 1 
print(start)
print(end)
