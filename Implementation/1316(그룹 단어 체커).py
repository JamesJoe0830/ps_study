import sys

N = int(input())


def check(str):
    distinc = []
    for s in str:
        if s not in distinc:
            distinc.append(s)
        else:
            if s == distinc[-1]:
                continue
            else:
                return 0
    return 1


cnt = 0
for i in range(N):
    str = input()
    if check(str):
        cnt += 1
print(cnt)
