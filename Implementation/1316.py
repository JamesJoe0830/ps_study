import sys
input = sys.stdin.readline
N = int(input())

answer = 0
for t in range(N):
  word = input()
  stack = []
  answer +=1 
  for w in word:
    if w not in stack:
      stack.append(w)
    else:
      if stack[-1] != w :
        answer -= 1
        break
      else :
        stack.append(w)

print(answer)

