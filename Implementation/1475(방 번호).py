import sys
input = sys.stdin.readline

N = input().rstrip()
nums = [0]*10
for i in N :
  if i == '9' :
    if nums[int(i)] > nums[6] :
      nums[6] +=1
    else :
      nums[int(i)] += 1
  elif i == '6' :
    if nums[int(i)] > nums[9] :
      nums[9] +=1
    else :
      nums[int(i)] += 1
  else:
    nums[int(i)] +=1
print(max(nums))

