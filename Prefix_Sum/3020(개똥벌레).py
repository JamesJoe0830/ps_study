import sys
input = sys.stdin.readline
# N : ë™êµ´ì˜ ê¸¸ì´ H: ë™êµ´ì˜ ë†’ì´
N, H = map(int,input().split())
heights = [0]*(H+1)
flag = True
bottom = [0]*(H+1) #ì•„ë˜ì„œ ìë¼ëŠ”ë†ˆ 
top = [0]*(H+1) # ìœ„ì—ì„œ ìë¼ëŠ”ë†ˆ 
